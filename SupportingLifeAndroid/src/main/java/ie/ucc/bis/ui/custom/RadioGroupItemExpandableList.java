package ie.ucc.bis.ui.custom;

import android.widget.RadioGroup;

public class RadioGroupItemExpandableList {

	private String itemLabel;
	private RadioGroup radioItemCategory;
	
	/**
	 * Getter Method: getItemLabel()
	 */	
	public String getItemLabel() {
		return itemLabel;
	}

	/**
	 * Setter Method: setItemLabel()
	 */	
	public void setItemLabel(String itemLabel) {
		this.itemLabel = itemLabel;
	}
	
	/**
	 * Getter Method: getRadioItemCategory()
	 */	
	public RadioGroup getRadioItemCategory() {
		return radioItemCategory;
	}
	
	/**
	 * Setter Method: setRadioItemCategory()
	 */	
	public void setRadioItemCategory(RadioGroup radioItemCategory) {
		this.radioItemCategory = radioItemCategory;
	}
	
}
